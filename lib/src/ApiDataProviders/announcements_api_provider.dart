        "errorCode": e.osError!.errorCode,
      };
    } on PlatformException catch (e) {
      print(e);
      return {
        "success": false,
        "message": "Something went wrong",
      };
    } catch (e) {
      print(e);
      return {
        "success": false,
        "message": "Something went wrong",
      };
    }
  }

